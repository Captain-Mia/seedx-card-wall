function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function normalize(value) {
  return String(value ?? "").trim().toLowerCase();
}

function getInitials(name) {
  return String(name).replace(/\s+/g, "").slice(0, 2).toUpperCase();
}

function getStatusClass(status) {
  const text = normalize(status);
  if (text.includes("gap")) return "status-gap";
  if (text.includes("创业")) return "status-coral";
  if (text.includes("自由")) return "status-purple";
  if (text.includes("学生")) return "status-blue";
  if (text.includes("新手") || text.includes("入行")) return "status-pink";
  if (text.includes("兼职") || text.includes("在职")) return "status-amber";
  return "status-gray";
}

function getStatusEmoji(status) {
  const text = normalize(status);
  if (text.includes("gap")) return "🌱";
  if (text.includes("创业")) return "🔥";
  if (text.includes("自由")) return "🪽";
  if (text.includes("学生")) return "🎓";
  if (text.includes("新手") || text.includes("入行")) return "✨";
  if (text.includes("兼职") || text.includes("在职")) return "💼";
  return "•";
}

function splitDirection(direction) {
  if (Array.isArray(direction)) return direction;
  return String(direction ?? "")
    .split("/")
    .map((item) => item.trim())
    .filter(Boolean);
}

function chipList(items, className) {
  return (items ?? [])
    .map((item) => `<span class="chip ${className}">${escapeHtml(item)}</span>`)
    .join("");
}

function personNameBadge(person) {
  if (person.tags?.length) {
    return person.tags
      .map((tag) => `<span class="person-special-tag">${escapeHtml(tag)}</span>`)
      .join("");
  }
  return `<span class="person-number">${escapeHtml(person.number)}</span>`;
}

function sectionTitle(icon, title) {
  return `<h3 class="section-title">${icon}<span>${title}</span></h3>`;
}

